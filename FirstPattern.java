package com.bridgelabz.secondpack;

public class FirstPattern {
////

	public static void main(String args[]) {

		int i, j, row = 5;

		for (i = 0; i < row; i++) // loop for rows
		{

			for (j = 0; j <= 8; j++) // loop for 8 columns
			{

				System.out.print("* ");
			}

			System.out.println();
		}
	}
}
